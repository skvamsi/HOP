 function cards1() {
    var inputDiv1 = document.getElementById('mb-id3');  
    var inputDiv2 = document.getElementById('mb-id4');  
    	if(inputDiv1.style.display == "" || inputDiv1.style.display == "none"){
    		inputDiv2.style.display = "none";
        	inputDiv1.style.display = "block";
    	}else {
    		inputDiv1.style.display = "none";
        	inputDiv2.style.display = "block";
    	}
        
    }
